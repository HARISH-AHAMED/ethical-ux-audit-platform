import { useState, useEffect } from 'react'
import { useLocation, useNavigate } from 'react-router-dom'
import jsPDF from 'jspdf'
import autoTable from 'jspdf-autotable'
import { 
  ArrowLeft, 
  AlertCircle, 
  CheckCircle, 
  AlertTriangle, 
  Shield,
  ArrowRight,
  ExternalLink,
  Info,
  Search,
  Download
} from 'lucide-react'
import { motion } from 'framer-motion'

function Results() {
  const location = useLocation()
  const navigate = useNavigate()
  
  // Use backend data if available, otherwise fallback to local mock/defaults
  const auditData = location.state?.auditData
  const scannedUrl = auditData?.url || location.state?.url || 'example.com'

  const score = auditData?.score || 64
  const patterns = auditData?.patterns || []

  const [displayScore, setDisplayScore] = useState(0)

  useEffect(() => {
    let start = 0;
    const duration = 1000;
    const increment = score / (duration / 16);
    
    const timer = setInterval(() => {
      start += increment;
      if (start >= score) {
        setDisplayScore(score);
        clearInterval(timer);
      } else {
        setDisplayScore(Math.floor(start));
      }
    }, 16);
    return () => clearInterval(timer);
  }, [score])

  const handleDownload = () => {
    const doc = new jsPDF()
    
    // Header
    doc.setFontSize(22)
    doc.setTextColor(15, 23, 42)
    doc.text("ETHICAL UX AUDIT REPORT", 14, 22)
    
    // Subheader
    doc.setFontSize(11)
    doc.setTextColor(100)
    doc.text(`Generated by Maverick Platform (Hacksagon 2026)`, 14, 30)
    
    // Details
    doc.setFontSize(12)
    doc.setTextColor(20)
    doc.text(`Target URL: ${scannedUrl}`, 14, 45)
    doc.text(`Date of Audit: ${new Date().toLocaleString()}`, 14, 52)
    
    // Score
    doc.setFontSize(14)
    doc.setFont(undefined, 'bold')
    const scoreColor = score >= 80 ? [16, 185, 129] : score >= 60 ? [245, 158, 11] : [239, 68, 68]
    doc.setTextColor(scoreColor[0], scoreColor[1], scoreColor[2])
    doc.text(`Ethical Score: ${score}/100`, 14, 62)
    doc.setFont(undefined, 'normal')
    
    // Table
    if (patterns && patterns.length > 0) {
      doc.setTextColor(20)
      doc.setFontSize(12)
      doc.text(`Detected Dark Patterns (${patterns.length})`, 14, 78)
      
      const tableData = patterns.map((p, i) => [
        i + 1,
        p.type,
        p.severity,
        p.description || 'N/A',
        p.suggestion || 'N/A'
      ])

      autoTable(doc, {
        startY: 85,
        head: [['#', 'Pattern Type', 'Severity', 'Description', 'Suggestion']],
        body: tableData,
        theme: 'grid',
        headStyles: { fillColor: [15, 23, 42], textColor: 255 },
        styles: { fontSize: 9, cellPadding: 4 },
        columnStyles: {
          0: { cellWidth: 10 },
          1: { cellWidth: 35 },
          2: { cellWidth: 20 },
          3: { cellWidth: 'auto' },
          4: { cellWidth: 'auto' }
        }
      })
    } else {
      doc.setTextColor(16, 185, 129)
      doc.text("✓ No dark patterns detected! Excellent ethical standing.", 14, 80)
    }

    doc.save(`Maverick_Audit_${scannedUrl.replace(/[^a-z0-9]/gi, '_').toLowerCase()}.pdf`)
  }

  // Map icon strings (if from backend) to Components, or use default
  const getIcon = (type) => {
    switch(type) {
      case 'Forced Sign-Up': return Shield
      case 'Fake Urgency': return AlertTriangle
      case 'Nagging Popups': return Info
      default: return AlertCircle
    }
  }

  const getStyles = (severity) => {
    switch(severity) {
      case 'High': return { color: 'text-red-500', bg: 'bg-red-500/10', border: 'border-red-500/20' }
      case 'Medium': return { color: 'text-orange-500', bg: 'bg-orange-500/10', border: 'border-orange-500/20' }
      case 'Low': return { color: 'text-blue-500', bg: 'bg-blue-500/10', border: 'border-blue-500/20' }
      default: return { color: 'text-slate-400', bg: 'bg-slate-500/10', border: 'border-slate-800' }
    }
  }

  return (
    <motion.main 
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.5 }}
      className="max-w-5xl mx-auto px-4 py-12"
    >
      {/* Header / Back Navigation */}
      <div className="flex flex-col md:flex-row md:items-center justify-between gap-6 mb-12">
        <div className="flex items-center gap-4">
          <button 
            onClick={() => navigate('/')}
            className="flex items-center gap-2 text-slate-400 hover:text-white transition-colors group px-4 py-2 bg-slate-900 border border-slate-800 rounded-xl"
          >
            <ArrowLeft className="w-5 h-5 group-hover:-translate-x-1 transition-transform" />
            <span className="hidden sm:inline">Back</span>
          </button>
          
          <a 
            href={scannedUrl.startsWith('http') ? scannedUrl : `https://${scannedUrl}`}
            target="_blank"
            rel="noopener noreferrer"
            className="flex items-center gap-3 bg-slate-900 px-4 py-2 rounded-xl border border-slate-800 shadow-xl hover:border-primary-500/30 transition-colors group cursor-pointer"
          >
            <div className="w-2 h-2 rounded-full bg-green-500 animate-pulse"></div>
            <span className="text-sm font-medium text-slate-300 truncate max-w-[150px] sm:max-w-xs group-hover:text-primary-400 transition-colors">{scannedUrl}</span>
            <ExternalLink className="w-4 h-4 text-slate-500 group-hover:text-primary-400 transition-colors" />
          </a>
        </div>

        <button 
          onClick={() => navigate('/')}
          className="bg-primary-600/10 hover:bg-primary-600/20 text-primary-400 border border-primary-500/20 px-6 py-2.5 rounded-xl text-sm font-bold transition-all flex items-center justify-center gap-2 active:scale-95"
        >
          <Search className="w-4 h-4" />
          Scan Another Website
        </button>
      </div>

      {/* Summary Score Card */}
      <div className="grid md:grid-cols-3 gap-8 mb-16">
        <div className="md:col-span-2 bg-slate-900 border border-slate-800 rounded-3xl p-8 flex flex-col justify-between relative overflow-hidden group">
          <div className="absolute top-0 right-0 p-8 opacity-10 group-hover:opacity-20 transition-opacity">
            <Shield className="w-40 h-40" />
          </div>
          <div>
            <h2 className="text-3xl font-bold mb-4">Audit Summary</h2>
            <p className="text-slate-400 max-w-md">
              We analyzed the provided URL for ethical UX violations. Our system detected 
              <span className="text-white font-semibold"> {patterns.length} dark patterns</span> with varying severity levels.
            </p>
          </div>
          <div className="mt-8 flex gap-4">
            <div className="bg-red-500/10 text-red-400 px-4 py-2 rounded-xl text-xs font-bold border border-red-500/20">
              1 HIGH SEVERITY
            </div>
            <div className="bg-orange-500/10 text-orange-400 px-4 py-2 rounded-xl text-xs font-bold border border-orange-500/20">
              1 MEDIUM SEVERITY
            </div>
          </div>
        </div>

        <div className="bg-slate-900 border border-slate-800 rounded-3xl p-8 flex flex-col items-center justify-center text-center">
          <div className="relative w-32 h-32 flex items-center justify-center">
            <svg className="w-full h-full transform -rotate-90">
              <circle
                cx="64"
                cy="64"
                r="58"
                stroke="currentColor"
                strokeWidth="8"
                fill="transparent"
                className="text-slate-800"
              />
              <circle
                cx="64"
                cy="64"
                r="58"
                stroke="currentColor"
                strokeWidth="8"
                fill="transparent"
                strokeDasharray={364.4}
                strokeDashoffset={364.4 * (1 - displayScore / 100)}
                className="text-primary-500 transition-all duration-75 ease-out"
                strokeLinecap="round"
              />
            </svg>
            <span className="absolute text-3xl font-black">{displayScore}</span>
          </div>
          <h3 className="mt-6 font-bold text-xl uppercase tracking-wider text-slate-500">Ethical Score</h3>
          <p className="mt-2 text-sm text-green-500 font-medium">Moderate Standing</p>
        </div>
      </div>

      {/* Detailed Issues */}
        <h3 className="text-xl font-bold flex items-center gap-2 mb-8">
          <AlertCircle className="w-6 h-6 text-primary-500" />
          Detected Dark Patterns
        </h3>

        <motion.div 
          initial="hidden"
          animate="show"
          variants={{
            hidden: { opacity: 0 },
            show: { opacity: 1, transition: { staggerChildren: 0.15 } }
          }}
          className="space-y-6"
        >
          {patterns.map((pattern, index) => {
            const Icon = getIcon(pattern.type)
            const styles = getStyles(pattern.severity)
            return (
              <motion.div 
                key={index}
                variants={{
                  hidden: { opacity: 0, x: -20 },
                  show: { opacity: 1, x: 0 }
                }}
                className={`p-1 rounded-3xl bg-gradient-to-br from-slate-800 to-slate-900 border border-slate-800 hover:border-slate-700 transition-all shadow-xl group`}
              >
              <div className="p-6 md:p-8 flex flex-col md:flex-row gap-8 items-start">
                <div className={`${styles.bg} ${styles.color} p-4 rounded-2xl`}>
                  <Icon className="w-8 h-8" />
                </div>
                
                <div className="flex-1 space-y-4">
                  <div className="flex items-center justify-between">
                    <h4 className="text-2xl font-bold">{pattern.type}</h4>
                    <span className={`text-xs font-black uppercase tracking-widest px-3 py-1 rounded-full border ${styles.border} ${styles.color}`}>
                      {pattern.severity}
                    </span>
                  </div>
                
                <p className="text-slate-400 leading-relaxed">
                  {pattern.description}
                </p>

                <div className="pt-4 border-t border-slate-800 flex flex-col md:flex-row md:items-center justify-between gap-4">
                  <div className="flex items-start gap-2">
                    <CheckCircle className="w-5 h-5 text-green-500 mt-1 flex-shrink-0" />
                    <div>
                      <p className="text-xs font-bold text-slate-500 uppercase tracking-tighter">Improvement Suggestion</p>
                      <p className="text-sm text-slate-200 mt-1">{pattern.suggestion}</p>
                    </div>
                  </div>
                  <button className="bg-slate-800 hover:bg-slate-700 text-white px-4 py-2 rounded-xl text-xs font-bold transition-colors flex items-center gap-2 group/btn">
                    Details <ArrowRight className="w-4 h-4 group-hover/btn:translate-x-1 transition-transform" />
                  </button>
                </div>
              </div>
              </div>
            </motion.div>
          )
        })}
        </motion.div>

      {/* Audit Suggestions Footer */}
      <div className="mt-20 p-10 bg-primary-600/5 border border-primary-500/10 rounded-3xl text-center">
        <h3 className="text-2xl font-bold mb-4">Want a full audit report?</h3>
        <p className="text-slate-400 mb-8 max-w-xl mx-auto">
          During the hackathon demo, we can show you how our backend will eventually process 
          multiple pages and generate a PDF report for business owners.
        </p>
        <button 
          onClick={handleDownload}
          className="bg-primary-600 hover:bg-primary-500 text-white px-10 py-4 rounded-2xl font-bold transition-all shadow-xl shadow-primary-500/20 active:scale-95 flex items-center justify-center gap-2 mx-auto"
        >
          <Download className="w-5 h-5" />
          Download Audit Report (.pdf)
        </button>
      </div>
    </motion.main>
  )
}

export default Results
